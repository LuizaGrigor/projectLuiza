package org.example;

public class Calculator
{
    public int addNumber(int numberX, int numberY)
    {
        return numberX + numberY;
    }

    public int substractionNumber(int numberX, int numberY)
    {
        return numberX - numberY;
    }
    public int multiplicationionNumber(int numberX, int numberY)
    {
        return numberX * numberY;
    }
    public int divisionNumber(int numberX, int numberY)
    {
        return numberX / numberY;
    }
}